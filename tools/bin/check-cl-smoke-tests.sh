#!/usr/bin/env bash
set -euo pipefail

CHAINLINK_REPO="chainlink-repo"
BASE_REF="origin/main"
FORCE_RUN="false"

usage() {
  echo "Usage: $0 [OPTIONS]"
  echo ""
  echo "Options:"
  echo "  --chainlink-repo PATH   Path to the chainlink repository (default: $CHAINLINK_REPO)"
  echo "  --base-ref REF          Base branch/ref to compare against for changed files (default: $BASE_REF)"
  echo "  --force-run             Force the tests to run regardless of changes"
  echo "  --help                  Show this help message"
  exit 0
}

while [[ $# -gt 0 ]]; do
  case $1 in
    --chainlink-repo)
      CHAINLINK_REPO="$2"
      shift 2
      ;;
    --base-ref)
      BASE_REF="$2"
      shift 2
      ;;
    --force-run)
      FORCE_RUN="true"
      shift 1
      ;;
    --help)
      usage
      ;;
    *)
      echo "Unknown option: $1"
      usage
      ;;
  esac
done

# Helper function to output the result
output_result() {
  local run_tests=$1
  if [[ -n "${GITHUB_OUTPUT:-}" ]]; then
    echo "run_tests=$run_tests" >> "$GITHUB_OUTPUT"
  fi
  echo "==> RESULT: run_tests=$run_tests"
  exit 0
}

# 1. Check for manual override
if [[ "$FORCE_RUN" == "true" ]] || [[ "${HAS_RUN_CL_SMOKE_LABEL:-false}" == "true" ]]; then
  echo "Manual override requested. Running tests."
  output_result "true"
fi

# 2. Get all changed files
# In CI, we use EVENT_NAME to decide. Locally, we use BASE_REF provided by args or default.
if [[ "${EVENT_NAME:-}" == "pull_request" && -n "${BASE_REF_CI:-}" ]]; then
  CHANGED_FILES=$(git diff --name-only origin/${BASE_REF_CI}...HEAD || true)
elif [[ "${EVENT_NAME:-}" == "push" ]]; then
  CHANGED_FILES=$(git diff --name-only HEAD~1...HEAD || true)
else
  # Local fallback
  CHANGED_FILES=$(git diff --name-only ${BASE_REF}...HEAD || true)
fi

echo "Changed files:"
echo "$CHANGED_FILES"

if [[ -z "$CHANGED_FILES" ]]; then
  echo "No changed files found."
  output_result "false"
fi

# 3. Find direct imports of chainlink-ccv in chainlink
if [[ ! -d "$CHAINLINK_REPO" ]]; then
  echo "Error: Chainlink repo directory not found at '$CHAINLINK_REPO'."
  echo "Please clone it or provide the correct path using --chainlink-repo"
  exit 1
fi

echo "Scanning $CHAINLINK_REPO for direct imports..."
DIRECT_IMPORTS=$(grep -R "github.com/smartcontractkit/chainlink-ccv" --include="*.go" "$CHAINLINK_REPO" | grep -o 'github.com/smartcontractkit/chainlink-ccv[^"]*' | sort -u || true)

echo "Direct CCV imports in Chainlink:"
echo "$DIRECT_IMPORTS"

if [ -z "$DIRECT_IMPORTS" ]; then
  echo "No direct imports found. Skipping tests."
  output_result "false"
fi

# 4. Find all transitive dependencies of these direct imports within chainlink-ccv
# We must be in the chainlink-ccv repo root for this to work
if [[ ! -f "go.mod" ]]; then
  echo "Error: go.mod not found. Please run this script from the root of the chainlink-ccv repository."
  exit 1
fi

go mod download

if ! ALL_DEPS=$(go list -deps $DIRECT_IMPORTS 2>/dev/null); then
  echo "go list failed, running tests to be safe."
  output_result "true"
fi

# filter only CCV packages
ALL_DEPS=$(echo "$ALL_DEPS" | grep 'github.com/smartcontractkit/chainlink-ccv' || true)
echo "All affected CCV packages:"
echo "$ALL_DEPS"

# 5. Convert packages to directories
AFFECTED_DIRS=$(echo "$ALL_DEPS" | sed -e 's|^github.com/smartcontractkit/chainlink-ccv/||' -e 's|^github.com/smartcontractkit/chainlink-ccv$|.|' | sort -u)
echo "Affected CCV directories:"
echo "$AFFECTED_DIRS"

# 6. Check if any changed file is in the affected directories
RUN_TESTS="false"

get_diff_target() {
  if [[ "${EVENT_NAME:-}" == "pull_request" && -n "${BASE_REF_CI:-}" ]]; then
    echo "origin/${BASE_REF_CI}...HEAD"
  elif [[ "${EVENT_NAME:-}" == "push" ]]; then
    echo "HEAD~1...HEAD"
  else
    echo "${BASE_REF}...HEAD"
  fi
}

for file in $CHANGED_FILES; do
  if [[ "$file" == "go.mod" ]]; then
     DIFF_TARGET=$(get_diff_target)
     # Extract any modules that were added or bumped/downgraded in go.mod
     CHANGED_MODULES=$(git diff $DIFF_TARGET -- go.mod | grep -E '^[+-]\s+[a-zA-Z0-9_.-]+' | awk '{print $2}' | sort -u || true)
     
     if [ -n "$CHANGED_MODULES" ]; then
       for mod in $CHANGED_MODULES; do
         # If the module name appears as a prefix in the ALL_DEPS list, it is used by Chainlink
         if echo "$ALL_DEPS" | grep -q "^$mod"; then
           RUN_TESTS="true"
           echo "Critical dependency '$mod' changed in go.mod. Running tests."
           break 2
         fi
       done
     fi
     echo "go.mod changed, but the modified dependencies are not used by Chainlink. Skipping tests for go.mod."
     continue
  fi
  
  if [[ "$file" == "go.sum" ]]; then
     # go.sum is autogenerated. If it changed but go.mod or CCV directories didn't trigger tests, it's safe to skip.
     continue
  fi
  
  file_dir=$(dirname "$file")
  
  if echo "$AFFECTED_DIRS" | grep -Fqx "$file_dir"; then
     RUN_TESTS="true"
     echo "Changed file $file is in an affected package $file_dir. Running tests."
     break
  fi
  if [[ "$file_dir" == "." ]] && echo "$AFFECTED_DIRS" | grep -Fqx "."; then
     RUN_TESTS="true"
     echo "Changed file $file is in the root affected package. Running tests."
     break
  fi
done

output_result "$RUN_TESTS"